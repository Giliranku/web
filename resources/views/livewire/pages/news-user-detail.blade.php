        </div>

        <div class="mb-5">
            <h3 class="fw-bolder fs-4">Akses Mudah dan Lokasi Strategis</h3>
            <p class="fs-5">Terletak di pesisir utara Jakarta, Ancol sangat mudah diakses dari berbagai penjuru kota. Baik menggunakan kendaraan pribadi, transportasi umum seperti TransJakarta, atau bahkan KRL, Anda dapat mencapai Ancol dengan relatif mudah. Lokasinya yang strategis ini menjadikannya pilihan ideal untuk liburan singkat maupun panjang tanpa perlu bepergian jauh ke luar kota.</p>
        </div>

        <div class="mb-5">
